import alias from '@rollup/plugin-alias';
import babel from '@rollup/plugin-babel';
import commonjs from '@rollup/plugin-commonjs';
import postcss from 'rollup-plugin-postcss';
import postcssImport from 'postcss-import';
import postcssPresetEnv from 'postcss-preset-env';
import replace from '@rollup/plugin-replace';
import resolve from '@rollup/plugin-node-resolve';
import terser from '@rollup/plugin-terser';
import path from 'path';

const isProduction = process.env.NODE_ENV === 'production';

// ------------------------------------------------------------------
//  Console methods to strip in production (keep info, warn, error)
// ------------------------------------------------------------------

const consolePureFuncs = Object.keys(console)
    .filter((key) => !['info', 'warn', 'error'].includes(key))
    .map((key) => `console.${key}`);

// ------------------------------------------------------------------
//  PostCSS plugins factory
// ------------------------------------------------------------------

const postcssPlugins = (namespace = '') => [
    postcssImport(),
    postcssPresetEnv({
        features: { 'custom-properties': false },
        stage: 1,
    }),
    ...(namespace
        ? [getNamespacePlugin(namespace)]
        : []
    ),
];

let _postcssSelectorNamespace;

try {
    _postcssSelectorNamespace = (await import('postcss-selector-namespace')).default;
} catch {
    _postcssSelectorNamespace = null;
}

const getNamespacePlugin = (namespace) => {
    if (!_postcssSelectorNamespace) {
        throw new Error(
            '@site-reviews/build: CSS namespacing requires "postcss-selector-namespace". ' +
            'Install it as a devDependency in your project.'
        );
    }
    return _postcssSelectorNamespace({ namespace });
};

// ------------------------------------------------------------------
//  Rollup plugin stacks
// ------------------------------------------------------------------

const jsPlugins = (rootDir, { cjs = false } = {}) => [
    alias({
        entries: [
            { find: '@', replacement: path.resolve(rootDir, '+/scripts') },
        ],
    }),
    replace({
        preventAssignment: true,
        'process.env.NODE_ENV': JSON.stringify(isProduction ? 'production' : 'development'),
    }),
    resolve(),
    ...(cjs ? [commonjs()] : []),
    babel({
        babelHelpers: 'runtime',
        plugins: [
            '@babel/plugin-transform-runtime',
        ],
        presets: [
            '@babel/preset-env',
        ],
    }),
    ...(isProduction
        ? [terser({
            compress: {
                pure_funcs: consolePureFuncs,
            },
            format: {
                comments: false,
            },
        })]
        : []
    ),
];

// Remove the stub JS files generated by rollup-plugin-postcss
// when processing CSS-only entries with extract: true.
const cleanCssJsPlugin = () => ({
    name: 'clean-css-js',
    generateBundle(_, bundle) {
        for (const fileName of Object.keys(bundle)) {
            if (fileName.endsWith('.js') || fileName.endsWith('.js.map')) {
                delete bundle[fileName];
            }
        }
    },
});

const cssPlugins = (namespace = '') => [
    postcss({
        extract: true,
        minimize: isProduction
            ? {
                minifyFontValues: false,
                discardComments: { removeAll: true },
                zindex: false,
            }
            : false,
        plugins: postcssPlugins(namespace),
        sourceMap: !isProduction,
    }),
    cleanCssJsPlugin(),
];

// ------------------------------------------------------------------
//  Config builders factory
// ------------------------------------------------------------------

export function createConfig(rootDir) {
    /**
     * JavaScript bundle (IIFE, no CommonJS transforms).
     *
     * @param {string} source     Path relative to `+/` without extension.
     * @param {string} [outputDir='assets/scripts']  Output directory.
     * @param {string} [filename]  Explicit output filename (without extension).
     *                              When set, uses `output.file`; otherwise `output.dir`.
     */
    const js = (source, outputDir = 'assets/scripts', filename = '') => ({
        input: `+/${source}.js`,
        output: {
            ...(filename
                ? { file: `${outputDir}/${filename}.js` }
                : { dir: outputDir }
            ),
            format: 'iife',
            sourcemap: !isProduction,
        },
        plugins: jsPlugins(rootDir),
    });

    /**
     * JavaScript bundle (IIFE, with CommonJS transforms).
     *
     * @param {string} source     Path relative to `+/` without extension.
     * @param {string} [outputDir='assets/scripts']  Output directory.
     * @param {string} [filename]  Explicit output filename (without extension).
     */
    const commonJs = (source, outputDir = 'assets/scripts', filename = '') => ({
        input: `+/${source}.js`,
        output: {
            ...(filename
                ? { file: `${outputDir}/${filename}.js` }
                : { dir: outputDir }
            ),
            format: 'iife',
            sourcemap: !isProduction,
        },
        plugins: jsPlugins(rootDir, { cjs: true }),
    });

    /**
     * CSS bundle with optional PostCSS selector namespacing.
     *
     * @param {string} source     Filename relative to `+/styles/` without extension.
     * @param {string} [outputDir='assets/styles']  Output directory.
     * @param {string} [namespace='']  CSS selector namespace (requires postcss-selector-namespace).
     */
    const css = (source, outputDir = 'assets/styles', namespace = '') => ({
        input: `+/${source}.css`,
        output: {
            dir: outputDir,
            format: 'es',
        },
        onwarn(warning, warn) {
            if (warning.code === 'FILE_NAME_CONFLICT') return;
            warn(warning);
        },
        plugins: cssPlugins(namespace),
    });

    /**
     * CSS bundle with optional PostCSS selector namespacing.
     *
     * @param {string} source     Filename relative to `+/styles/` without extension.
     * @param {string} [outputDir='assets/styles']  Output directory.
     * @param {string} [namespace='']  CSS selector namespace (requires postcss-selector-namespace).
     */
    const namespacedCss = (source, namespace, outputDir = 'assets/styles') => css(source, outputDir, namespace);

    /**
     * Copy a static file to an output directory.
     *
     * @param {string} source     Path relative to project root.
     * @param {string} [outputDir='assets']  Output directory.
     */
    const copy = (source, outputDir = 'assets') => ({
        _copy: true,
        src: `+/${source}`,
        dest: outputDir,
    });

    return {
        commonJs,
        copy,
        css,
        js,
        namespacedCss,
    };
}
