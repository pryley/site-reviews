<p class="glsr-heading">fallback</p>
<p>Include the "fallback" option to change the text that is shown when the review is not found. This option will override the default fallback text.</p>
<p>The default fallback value is: <code>Review not found.</code></p>
<div class="shortcode-example">
    <pre><code class="language-shortcode">[site_review fallback="Invalid review."]</code></pre>
</div>
