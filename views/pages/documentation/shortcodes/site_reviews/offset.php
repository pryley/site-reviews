<p class="glsr-heading">offset</p>
<p>Include the "offset" option to displace or pass over a number of reviews.</p>
<p>For example, <code>[site_reviews display="5" offset="2"]</code> will show 5 reviews, skipping the first two. It is NOT recommended to use this option with pagination enabled.</p>
<p>The default offset value is: <code>0</code></p>
<div class="shortcode-example">
    <pre><code class="language-shortcode">[site_reviews offset="1"]</code></pre>
</div>
